/* 지정한 기술 배열을 그대로 포켓몬에게 복사합니다. */
void setFixedMoves(BattlePokemon *pokemon, Move moves[], int moveCount)
{
    clearMoves(pokemon);

    if (moveCount > MOVE_SLOT_COUNT) {
        moveCount = MOVE_SLOT_COUNT;
    }

    for (int i = 0; i < moveCount; i++) {
        addMove(pokemon, moves[i]);
    }
}

